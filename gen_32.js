const TNR = "Times New Roman";
const SZ = 28; // 14pt = 28 half-points

// Helpers
function body(text, indent = true) {
  return new Paragraph({
    alignment: AlignmentType.JUSTIFIED,
    spacing: { line: 360 }, // 1.5
    indent: indent ? { firstLine: 720 } : {},
    children: [new TextRun({ text, font: TNR, size: SZ })]
  });
}

function bold(text) {
  return new Paragraph({
    alignment: AlignmentType.JUSTIFIED,
    spacing: { line: 360 },
    indent: { firstLine: 720 },
    children: [new TextRun({ text, font: TNR, size: SZ, bold: true })]
  });
}

function h2(text) {
  return new Paragraph({
    alignment: AlignmentType.LEFT,
    spacing: { line: 360, before: 120, after: 120 },
    indent: { firstLine: 720 },
    children: [new TextRun({ text, font: TNR, size: SZ, bold: true })]
  });
}

function placeholder(figNum, caption) {
  return [
    new Paragraph({
      alignment: AlignmentType.CENTER,
      spacing: { line: 360, before: 120, after: 60 },
      children: [new TextRun({ text: "[ ВСТАВИТЬ РИСУНОК ]", font: TNR, size: SZ })]
    }),
    new Paragraph({
      alignment: AlignmentType.CENTER,
      spacing: { line: 360, after: 120 },
      children: [new TextRun({ text: `Рисунок ${figNum} – ${caption}`, font: TNR, size: SZ })]
    })
  ];
}

function empty() {
  return new Paragraph({ spacing: { line: 360 }, children: [new TextRun({ text: "", font: TNR, size: SZ })] });
}

